# Como hacen los gatos
animal = input("Ingrese el sonido de animal que quiera > ")

if	animal == "gato":
	print ("Los gatos hacen < Mau >")
	print ("Ya que solo los gatos maullan")

elif animal == "perro":
	print ("Los perros hacen < guau >")
	print ("Ya que los perros ladran")

elif animal == "vaca":
	print ("Las vacas hacen < muuu >")
	print ("Ya que las vacas mugen")

elif animal == "gallo":
	print ("Los gallos hacen < kikiriki >")
	print ("Ya que los pollos algo")

else:
	print ("El animal no existe =P")
	print ("naaa en realidad no conozco mas sobre como hacen los animales ;P")

